package Operators;

public class relationalOperators {
    public static void main(String[] args) {
        int num1 = 12;
        int num2 = 13;

        // is equal to
        System.out.println(num1 == num2); // false

        // is not eual to
        System.out.println(num1 != num2); // true

        // is greater than
        System.out.println(num1 > num2); // false 

        //is less than
        System.out.println(num1 < num2);// true

        // is greater than or equal to
        System.out.println(num1 >= num2); // false

        // less than or equal to
        System.out.println(num1 <= num2); // true
    }
}
