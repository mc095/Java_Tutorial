package Operators;

public class arithmeticOperators {
    public static void main(String[] args) {
        
        int num1 = 12;
        int num2 = 6;

        // Addition
        System.out.println(num1 + num2);

        // Subtraction
        System.out.println(num1 - num2);

        // Multiplication
        System.out.println(num1 * num2);

        // Division
        System.out.println(num1 / num2);

        // Modulo
        System.out.println(num1 % num2);
    }
}
